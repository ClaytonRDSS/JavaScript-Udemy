let calculadora = {
    soma: function(a, b) { return a + b;},
    subtrair: function(a, b) { return a - b;},
    multiplicar: function(a, b) { return a * b;},
    dividir: function(a, b) { return a / b;}
}

console.log(calculadora.soma(5, 4));
console.log(calculadora.subtrair(5, 4));
console.log(calculadora.multiplicar(5, 4));
console.log(calculadora.dividir(4, 4));

