let a = 3;
let b = 4;


// Estrutura de controle usando eslseIf
if( a + b == 3 ) {
    console.log("O resultado é 3");
} else if( a + b == 4 ) {
    console.log("O resultado é 4");
} else if( a + b == 5 ) {
    console.log("O resultado é 5");
} else {
    console.log("Nem uma das condições a cima");
}