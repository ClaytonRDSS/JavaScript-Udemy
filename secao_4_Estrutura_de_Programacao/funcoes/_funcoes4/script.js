// Funcão built0-in: Math;

// Retorna o mair valor;
let maior = Math.max(6, 12, 28, 55, 8)

console.log(maior);

// Retorna o menor numero;
let menor = Math.min(9, 12, 28, 55, 8);

console.log(menor);

// Retorna um numero arredondado;
let arredondar = Math.round(5.2934723);

console.log(arredondar);

// Retorna um numero arredondado o número pra cima;
let arredondarPraCima = Math.ceil(5.2934723);

console.log(arredondarPraCima);